int	ft_iterative_factorial(int nb)
{
	int i;
	int y;

	i = 1;
	y = 1;
	if (nb < 0)
		return (0);
	while (nb >= 1)
	{
		y = y * nb;
		nb--;
	}
	return (y);
}
